package com.lemur.eva.core.constant;

/**
 * 时间常量
 */
public interface DateConstant {

    /**
     * 1000毫秒
     */
    int ONE_THOUSAND_MILLISECONDS = 1000;

    /**
     * 1天_秒制
     */
    int ONE_DAY_SECONDS = 60 * 60 * 24;

    /**
     * 1小时_秒制
     */
    int ONE_HOUR_SECONDS = 60 * 60;

    /**
     * 6小时_秒制
     */
    int SIX_HOUR_SECONDS = 60 * 60 * 6;

    /**
     * 12小时_秒制
     */
    int HALF_DAY_SECONDS = 60 * 60 * 12;

    /**
     * 永不过期
     */
    int NOT_EXPIRE = -1;
}
